'use client';

import React, { useState } from 'react';

type Props = {
  variant: string;
  color: string;
  hoverColor?: string;
};

export default function Icons({ variant, color, hoverColor }: Props) {
  const [isHovered, setIsHovered] = useState(false);

  const getIcon = () => {
    switch (variant) {
      case 'GitHub':
        return (
          <GitHubIcon color={isHovered && hoverColor ? hoverColor : color} />
        );
      case 'Discord':
        return <Discord color={isHovered && hoverColor ? hoverColor : color} />;
      case 'Instagram':
        return (
          <Instagram color={isHovered && hoverColor ? hoverColor : color} />
        );
      case 'LinkedIn':
        return (
          <LinkedIn color={isHovered && hoverColor ? hoverColor : color} />
        );
      case 'Npm':
        return <Npm color={isHovered && hoverColor ? hoverColor : color} />;
      case 'Steam':
        return <Steam color={isHovered && hoverColor ? hoverColor : color} />;
      case 'Threads':
        return <Threads color={isHovered && hoverColor ? hoverColor : color} />;
      case 'WhatsApp':
        return (
          <WhatsApp color={isHovered && hoverColor ? hoverColor : color} />
        );
      case 'Email':
        return <Email color={isHovered && hoverColor ? hoverColor : color} />;
      case 'Xing':
        return <Xing color={isHovered && hoverColor ? hoverColor : color} />;
      default:
        return null;
    }
  };

  return (
    <span
      onMouseEnter={() => setIsHovered(true)}
      onMouseLeave={() => setIsHovered(false)}
    >
      {getIcon()}
    </span>
  );
}

